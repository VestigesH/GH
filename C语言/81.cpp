//题目：某个公司采用公用电话传递数据，数据是四位的整数，在传递过程中是加密的，加密规则如下：每位数字都加上5,
//然后用和除以10的余数代替该数字,再将第一位和第四位交换，第二位和第三位交换。
#include <stdio.h>
int main()
{ 
	int a,b,c,d,t1,t2;
	long x;
	printf("请输入四位整数:");
	scanf("%ld",&x);
	if (x>999&&x<10000)	
	{
		a=x/1000;
		b=x/100%10;
		c=x/10%10;
		d=x%10;
		a=(a+5)%10;
		b=(b+5)%10;
		c=(c+5)%10;
		d=(d+5)%10;
		t1=a;
		a=d;
		d=t1;
		t2=b;
		b=c;
		c=t2;
		printf("加密后结果为%d%d%d%d",a,b,c,d);
	}
	else printf("输入错误");	
} 
