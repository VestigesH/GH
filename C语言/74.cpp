/*题目：海滩上有一堆桃子，五只猴子来分。第一只猴子把这堆桃子平均分为五份，多了一个，这只猴子把多的一个扔入海中，拿走了一份。
第二只猴子把剩下的桃子又平均分成五份，又多了一个，它同样把多的一个扔入海中，拿走了一份，第三、第四、第五只猴子都是这样做的，
问海滩上原来最少有多少个桃子？*/ 
#include <stdio.h>
int main()
{
	int i=0,j=1,sum=1;
	while(1)
	{
		j=sum;
		for(i=0;i<5;i++)
		{
			if((j-1)%5==0) j=(j-1)/5*4;
			else break;
		}
		if(i==5&&j>0) break;
		sum+=1;
	}
	printf("桃子最少有%d个", sum);
	return 0;
}


